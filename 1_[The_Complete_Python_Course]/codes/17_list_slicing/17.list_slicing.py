tuple = (1, 2, 3, 4, 5, 6)

# print from 2nd to last
print(tuple[1:])

# print from 1st to third
print(tuple[:3])

# print from 2nd to third element
print(tuple[2:4])

# print from last third to before 6th index
print(tuple[-3:6])

# print nothing
print(tuple[-3:-6])

# print 4 3 2
print(tuple[-3:-6:-1])
